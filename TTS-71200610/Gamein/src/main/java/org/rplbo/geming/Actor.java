package org.rplbo.geming;

public class Actor {
    protected float x_pos;
    protected float y_pos;

    public void getinfo(){
    }

    public float getX_pos() {
        return x_pos;
    }

    public void setX_pos(float x_pos) {
        this.x_pos = x_pos;
    }

    public float getY_pos() {
        return y_pos;
    }

    public void setY_pos(float y_pos) {
        this.y_pos = y_pos;
    }
}
